# Arithmetic Operators
# +, -, *, /, %
a = 180
b = 30
print(a + b)
print(a - b)
print(a * b)
print(a / b) # Always gives float value
print(a ** b) # Power of a to the power of b
print(a // b) # Gives quotient



# Modulus Operator - It gives remainder also called as MOD
print(a % b)
# 90| 180 | 2
